#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	char		*dest;

	i = 0;
	dest = ft_calloc((ft_strlen(s) + 1), sizeof(char));
	if (!dest)
		return (NULL);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
