#include "push_swap.h"

void	print_list(t_list *list)
{
	if (!list)
	{
		ft_printf("empty list\n");
		return ;
	}
	ft_printf("\n**********************************************************\n");
	while (list)
	{
		ft_printf("%d\n", list->content);
		list = list->next;
	}
	ft_printf("**********************************************************\n");
}
